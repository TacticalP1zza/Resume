CREATE TABLE Customer (
    Customer_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    fname VARCHAR2(255),
    lname VARCHAR2(255),
    customer_password VARCHAR2(255),
    customer_email VARCHAR2(255),
    Address VARCHAR2(255),
    Telno VARCHAR2(20)
);

CREATE TABLE Store (
    Store_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    name VARCHAR2(255),
    location VARCHAR2(255)
);

CREATE TABLE Employee (
    Employee_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    Store_ID INT,
    employee_name VARCHAR2(255),
    role VARCHAR2(255),
    Telno VARCHAR2(20),
    FOREIGN KEY (Store_ID) REFERENCES Store(Store_ID)
);


CREATE TABLE COrder (
    Order_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    Store_ID NUMBER,
    Customer_ID NUMBER,
    Employee_ID NUMBER,
    quantity INT,
    OrderDate DATE,
    Status VARCHAR2(255),
    FOREIGN KEY (Store_ID) REFERENCES Store(Store_ID),
    FOREIGN KEY (Customer_ID) REFERENCES Customer(Customer_ID),
    FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID)
);

CREATE TABLE Product (
    Product_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    name VARCHAR2(255),
    descripition VARCHAR2(4000),
    price DECIMAL(10, 2),
    quantity INT
);

CREATE TABLE CollectedProduct (
    Product_ID NUMBER PRIMARY KEY,
    weight DECIMAL(10, 2),
    FOREIGN KEY (Product_ID) REFERENCES Product(Product_ID) ON DELETE CASCADE
);

CREATE TABLE DelieveredProduct (
    Product_ID NUMBER PRIMARY KEY,
    dimensions VARCHAR2(255),
    FOREIGN KEY (Product_ID) REFERENCES Product(Product_ID) ON DELETE CASCADE
);

CREATE TABLE Supplier (
    Supplier_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    Store_ID NUMBER,
    name VARCHAR2(255),
    telno VARCHAR2(20),
    supplier_email VARCHAR2(255),
    FOREIGN KEY (Store_ID) REFERENCES Store(Store_ID)
);

CREATE TABLE SupplierShipments (
    Shipment_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    Product_ID NUMBER,
    Supplier_ID NUMBER,
    DateOfDelivery DATE,
    Quantity INT,
    FOREIGN KEY (Product_ID) REFERENCES Product(Product_ID),
    FOREIGN KEY (Supplier_ID) REFERENCES Supplier(Supplier_ID)
);

CREATE TABLE DeliveryDetails (
    Order_ID NUMBER PRIMARY KEY,
    DeliveryAddress VARCHAR2(255),
    DeliveryDate DATE,
    TrackingNo VARCHAR2(255),
    PostageProvider VARCHAR2(255),
    FOREIGN KEY (Order_ID) REFERENCES COrder(Order_ID) ON DELETE CASCADE
);

